fun main(args: Array<String>) {
    println("Hello World!")

    var a: Int = 5
    println("Integer Variable : $a")
    var b: Float = 1.5F
    println("Float Value : $b")
    var c: Double = 95.35
    println("Double Variable : $c")
    var d: Char ='S'
    println("Character Value : $d")
    var e: Long = 2225624
    println("Long Value : $e")
    var f: String = "Vansh"
    println("String Value : $f")
    var g: Boolean = false
    println("Boolean Value : $g")
    var h: Short = -67
    println("Short Value : $h")
    var i: Byte = 127
    println("Byte Value : $i")
}